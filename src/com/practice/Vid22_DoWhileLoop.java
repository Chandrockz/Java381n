package com.practice;

public class Vid22_DoWhileLoop {
    public static void main(String[] args) {
        int a = 1;
        do {            // executes first and then checks the condition
            System.out.println(a);
            a++;
        }while (a>5);

        int b =0;
        do{
            System.out.println(b);
            b++;
        }while (b<401);

    }
}
